"use strict";

const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

const ROOT = path.resolve(__dirname, "..");
const APPS_DIR = path.join(ROOT, "apps");

function check(gameName) {
  const appDir = path.join(APPS_DIR, gameName);
  const results = { pass: [], warn: [], fail: [] };
  const add = (status, msg, detail) => {
    results[status].push({ msg, detail });
  };

  if (!fs.existsSync(appDir)) {
    add("fail", "App directory missing", "apps/" + gameName);
    return results;
  }

  const metadataDir = path.join(appDir, "metadata");
  const assetsDir = path.join(appDir, "assets");
  const srcDir = path.join(appDir, "src");
  const appConfigPath = path.join(appDir, "app.config.ts");

  if (!fs.existsSync(path.join(metadataDir, "appstore.json"))) {
    add("fail", "App Store metadata missing", "metadata/appstore.json");
  } else {
    add("pass", "App Store metadata exists", "metadata/appstore.json");
  }
  if (!fs.existsSync(path.join(metadataDir, "playstore.json"))) {
    add("fail", "Play Store metadata missing", "metadata/playstore.json");
  } else {
    add("pass", "Play Store metadata exists", "metadata/playstore.json");
  }

  const screenshotsDir = path.join(metadataDir, "screenshots");
  if (!fs.existsSync(screenshotsDir)) {
    add("warn", "Screenshots directory missing", "metadata/screenshots/");
  } else {
    const screens = fs.readdirSync(screenshotsDir).filter((f) => /\.(png|jpg|jpeg)$/i.test(f));
    if (screens.length === 0) add("warn", "No screenshot images", "metadata/screenshots/");
    else add("pass", "Screenshots present", screens.length + " file(s)");
  }

  const requiredAssets = ["icon.png", "splash.png"];
  for (const a of requiredAssets) {
    if (!fs.existsSync(path.join(assetsDir, a))) {
      add("fail", "Required asset missing", "assets/" + a);
    } else {
      add("pass", "Asset present", "assets/" + a);
    }
  }
  if (!fs.existsSync(path.join(assetsDir, "adaptive-icon.png"))) {
    add("warn", "Android adaptive icon missing", "assets/adaptive-icon.png");
  } else {
    add("pass", "Adaptive icon present", "assets/adaptive-icon.png");
  }

  if (!fs.existsSync(appConfigPath)) {
    add("fail", "app.config.ts missing", "");
  } else {
    const configContent = fs.readFileSync(appConfigPath, "utf8");
    const hasName = /name:\s*["']/.test(configContent);
    const hasSlug = /slug:\s*["']/.test(configContent);
    const hasIos = /bundleIdentifier:/.test(configContent);
    const hasAndroid = /package:\s*["']/.test(configContent);
    if (hasName && hasSlug && hasIos && hasAndroid) {
      add("pass", "app.config.ts identifiers present", "name, slug, ios, android");
    } else {
      add("fail", "app.config.ts missing identifiers", "name, slug, bundleIdentifier, package");
    }
  }

  if (!fs.existsSync(path.join(srcDir, "features.ts"))) {
    add("fail", "Feature flags file missing", "src/features.ts");
  } else {
    add("pass", "Feature flags file exists", "src/features.ts");
  }

  const i18nDir = path.join(srcDir, "i18n");
  if (!fs.existsSync(i18nDir)) {
    add("warn", "i18n directory missing", "src/i18n/");
  } else {
    const jsonCount = fs.readdirSync(i18nDir).filter((f) => f.endsWith(".json")).length;
    if (jsonCount === 0) add("warn", "No i18n JSON files", "src/i18n/");
    else add("pass", "i18n files present", jsonCount + " locale(s)");
  }

  if (!fs.existsSync(path.join(srcDir, "theme.ts"))) {
    add("warn", "Theme file missing", "src/theme.ts");
  } else {
    add("pass", "Theme file exists", "src/theme.ts");
  }

  try {
    execSync("pnpm --filter @bitcraft/" + gameName + " typecheck", {
      cwd: ROOT,
      stdio: "pipe",
      encoding: "utf8",
    });
    add("pass", "Typecheck passed", "");
  } catch (e) {
    add("fail", "Typecheck failed", "Run: pnpm --filter @bitcraft/" + gameName + " typecheck");
  }

  try {
    execSync("npx expo config --type public", {
      cwd: appDir,
      stdio: "pipe",
      encoding: "utf8",
    });
    add("pass", "Expo config valid", "");
  } catch (e) {
    add("fail", "Expo config invalid", "Run: npx expo config --type public");
  }

  return results;
}

function buildMarkdown(gameName, results) {
  const lines = [
    "# Release readiness: " + gameName,
    "",
    "Generated by `pnpm check-release-readiness " + gameName + "`",
    "",
    "## Summary",
    "",
    "| Status | Count |",
    "|--------|-------|",
    "| PASS   | " + results.pass.length + " |",
    "| WARN   | " + results.warn.length + " |",
    "| FAIL   | " + results.fail.length + " |",
    "",
    "## PASS",
    "",
  ];
  for (const { msg, detail } of results.pass) {
    lines.push("- " + msg + (detail ? " — " + detail : ""));
  }
  lines.push("", "## WARN", "");
  for (const { msg, detail } of results.warn) {
    lines.push("- " + msg + (detail ? " — " + detail : ""));
  }
  lines.push("", "## FAIL", "");
  for (const { msg, detail } of results.fail) {
    lines.push("- " + msg + (detail ? " — " + detail : ""));
  }
  lines.push(
    "",
    "## Next steps",
    "",
    results.fail.length > 0
      ? "1. Fix all FAIL items before submitting to stores."
      : "1. Address WARN items if needed (e.g. add real screenshots).",
    "2. Run `pnpm generate-screenshots " + gameName + "` if placeholders are acceptable, or replace with real screenshots.",
    "3. Fill in store metadata (title, description, keywords) in metadata/appstore.json and metadata/playstore.json.",
    "4. Build with EAS or expo build and submit to App Store Connect / Google Play Console.",
    ""
  );
  return lines.join("\n");
}

function main() {
  const gameName = process.argv[2]?.trim().toLowerCase();
  if (!gameName) {
    console.error("Usage: pnpm check-release-readiness <game-name>");
    console.error("Example: pnpm check-release-readiness flappy");
    process.exit(1);
  }

  const results = check(gameName);

  console.log("");
  console.log("Release readiness: " + gameName);
  console.log("  PASS: " + results.pass.length);
  console.log("  WARN: " + results.warn.length);
  console.log("  FAIL: " + results.fail.length);
  console.log("");
  for (const { msg, detail } of results.fail) {
    console.log("  [FAIL] " + msg + (detail ? " — " + detail : ""));
  }
  for (const { msg, detail } of results.warn) {
    console.log("  [WARN] " + msg + (detail ? " — " + detail : ""));
  }
  console.log("");

  const metadataDir = path.join(APPS_DIR, gameName, "metadata");
  fs.mkdirSync(metadataDir, { recursive: true });
  const mdPath = path.join(metadataDir, "release-readiness.md");
  fs.writeFileSync(mdPath, buildMarkdown(gameName, results));
  console.log("Wrote " + mdPath);

  if (results.fail.length > 0) {
    process.exit(1);
  }
}

main();
